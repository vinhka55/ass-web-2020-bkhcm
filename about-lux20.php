        <h2>
          <span class="ez-toc-section" id="Noi_that_xe_Vinfast_Lux_A2_0"></span>
          Nội thất xe Vinfast Lux A2.0
          <span class="ez-toc-section-end"></span>
        </h2>

        <p style="text-align: center;">
          <img src="assets/img/LuxA2.0/LuxA2.08.jpg" alt="luxa208"><br><br>
          Khoang lái của Vinfast Lux A2.0 được thiết kế sang trọng

        <p>
          Khoang lái được thiết kế với táp-lô đẳng cấp chất liệu nhựa carbon, các chi tiết ốp kim loại và 
          phủ nội thất da cao cấp. Ghế lái và ghế phụ chỉnh điện 12 hướng trên bản cao cấp Premium, chỉnh 
          điện 4 hướng và cơ 2 hướng với 2 bản Standard, Plus. Nội thất xư sử dụng tong màu cam làm chủ đạo 
          với màn hình màu sắc nét với kích thước 7 inch ngay giữa. Hơn nửa, với các tiện ích đi kèm như 
          sau:
          <ul>
            <li>Màn hình cảm ứng 10.4 inch</li>
            <li>Chức năng định vị, bản đồ</li>
            <li>Raido, AM/FM</li>
            <li>Kết nối Wifi, 4 cổng USB, Bluetooth</li>
            <li>Sạc điện thoại không dây</li>
            <li>Dàn âm thanh 8 loa hoặc 13 loa có âm-ly</li>
            <li>Ổ cắm điện 12V, ổ điện xoay chiều 230V</li>
          </ul>
        </p>

